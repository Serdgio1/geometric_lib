"""
This module provides functions to calculate the area and perimeter of a triangle.

Functions:
- area(a, h): Returns the area of a triangle with base a and height h.
- perimeter(a, b, c): Returns the perimeter of a triangle with sides a, b, and c.
"""

def area(a, h): 
    return a * h / 2 

def perimeter(a, b, c): 
    return a + b + c 
