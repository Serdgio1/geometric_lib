import math

"""
This module provides functions to calculate the area and perimeter of a circle.

Functions:
- area(r): Returns the area of a circle with radius r.
- perimeter(r): Returns the perimeter (circumference) of a circle with radius r.
"""
import math

def area(r):
    return math.pi * r * r


def perimeter(r):
    return 2 * math.pi * r

