"""
This module provides functions to calculate the area and perimeter of a rectangle.

Functions:
 - area(a, b): Returns the area of a rectangle with sides a and b.
 - perimeter(a, b): Returns the perimeter of a rectangle with sides a and b.
"""
 
def area(a, b): 
    return a * b 

def perimeter(a, b): 
    return 2 * a + 2 *  b 

